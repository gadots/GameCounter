export type TranslationKey = keyof typeof translations.es;

export function t(
  key: TranslationKey,
  lang: Language,
  vars?: Record<string, string | number>
): string {
  const dict = translations[lang] as Record<string, string>;
  const template = dict[key] ?? (translations.es as Record<string, string>)[key] ?? key;
  if (!vars) return template;
  return Object.entries(vars).reduce(
    (s, [k, v]) => s.replaceAll(`{${k}}`, String(v)),
    template
  );
}

export function tTag(tag: string, lang: Language): string {
  const esDict = translations.es as Record<string, string>;
  const dict = translations[lang] as Record<string, string>;
  return dict[`tag.${tag}`] ?? esDict[`tag.${tag}`] ?? tag;
}
